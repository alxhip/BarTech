    private void ItemInfo(String name, DefaultTableModel model, int Price) {        //adds items in the table on button click
        Object[] itemInfo = new Object[5];
        int rows = jTable2.getRowCount();
        //System.out.println(rows);

        int i = 0;
        boolean flag = true;
        while (i < rows && flag == true) {
            if ((jTable2.getValueAt(i, 1)).equals(name)) {
                int quantity = Integer.parseInt(model.getValueAt(i, 2).toString());
                quantity++;
                model.setValueAt(quantity, i, 2);
                model.setValueAt(quantity * Price, i, 4);
                flag = false;
            }
            i++;
        }
        if (flag == true) {
            itemInfo[0] = count;
            itemInfo[1] = name;
            itemInfo[2] = 1;
            itemInfo[3] = Price;
            itemInfo[4] = (Integer.parseInt(itemInfo[2].toString()) * Price);
            model.addRow(itemInfo);
            count++;
        }
    }

